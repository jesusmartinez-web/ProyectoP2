    void cargarTabla() {
        javax.swing.table.DefaultTableModel modelo =
            (javax.swing.table.DefaultTableModel) tablaPrestamo1.getTabla().getModel();
        modelo.setRowCount(0);
        for (Clases.Model.Prestamo p : Controller.ControladorView.obtenerPrestamos()) {
            modelo.addRow(new Object[]{
                p.getAlumno(),
                String.join(", ", p.getLibros()),
                p.getId(),
                p.getFechaDePrestamo(),
                p.getFechaDevolucion(),
                p.getFechaDevuelta() != null ? p.getFechaDevuelta() : "Pendiente",
                p.verificarVencimiento()
            });
        }
    }
    
    private void botonDevolverAction() {
        String idStr = campoBusqueda.getText().trim();
        String fecha  = campoFecha.getText().trim();

        if (idStr.isEmpty() || fecha.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Completá el ID y la fecha de devolución.");
            return;
        }

        try {
            int id = Integer.parseInt(idStr);
            boolean ok = Controller.ControladorView.devolverPrestamo(id, fecha);
            if (ok) {
                campoBusqueda.setText("");
                campoFecha.setText("");
                cargarTabla(); // refresca la tabla con el estado actualizado
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "El ID debe ser un número.");
        }
    }


